package app.qwertz.qwertzcore.commands;

import app.qwertz.qwertzcore.QWERTZcore;
import org.bukkit.Bukkit;
import org.bukkit.ChatColor;
import org.bukkit.command.Command;
import org.bukkit.command.CommandExecutor;
import org.bukkit.command.CommandSender;
import org.bukkit.entity.Player;

public class AddWinCommand implements CommandExecutor {

    private final QWERTZcore plugin;

    public AddWinCommand(QWERTZcore plugin) {
        this.plugin = plugin;
    }

    @Override
    public boolean onCommand(CommandSender sender, Command command, String label, String[] args) {
        if (args.length != 1) {
            sender.sendMessage(ChatColor.RED + "Usage: /addwin <player>");
            return true;
        }

        Player target = Bukkit.getPlayer(args[0]);
        if (target == null) {
            sender.sendMessage(ChatColor.RED + "Player not found.");
            return true;
        }

        plugin.getDatabaseManager().addWin(target.getUniqueId());
        int wins = plugin.getDatabaseManager().getWins(target.getUniqueId());

        Bukkit.broadcastMessage(QWERTZcore.CORE_ICON + ChatColor.GREEN + " Added a win for " + target.getName() + ". They now have " + wins + " win(s).");
        target.sendMessage(ChatColor.GREEN + "You have been awarded a win! You now have " + wins + " win(s).");

        return true;
    }
}